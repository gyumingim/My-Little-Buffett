def search_companies(query: str, limit: int = 10) -> list[dict]:
    """기업 검색 (이름, 종목코드, 업종)"""
    q = query.lower()
    results = []
    for corp_code, corp_name, stock_code, sector in COMPANIES:
        if q in corp_name.lower() or q in stock_code.lower() or q in sector.lower():
            results.append({
                "corp_code": corp_code,
                "corp_name": corp_name,
                "stock_code": stock_code,
                "sector": sector,
            })
            if len(results) >= limit:
                break
    return results


def get_company_by_code(corp_code: str) -> dict | None:
    """고유번호로 기업 조회"""
    for code, name, stock, sector in COMPANIES:
        if code == corp_code:
            return {"corp_code": code, "corp_name": name, "stock_code": stock, "sector": sector}
    return None


def get_all_companies() -> list[dict]:
    """전체 기업 목록"""
    return [
        {"corp_code": c, "corp_name": n, "stock_code": s, "sector": t}
        for c, n, s, t in COMPANIES
    ]
