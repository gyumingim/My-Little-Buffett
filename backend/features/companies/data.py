def search_companies(query: str, limit: int = 10) -> list[dict]:
    """기업 검색"""
    query_lower = query.lower()
    results = []

    for corp_code, corp_name, stock_code, sector in COMPANIES:
        if query_lower in corp_name.lower() or query_lower in stock_code:
            results.append({
                "corp_code": corp_code,
                "corp_name": corp_name,
                "stock_code": stock_code,
                "sector": sector,
            })
            if len(results) >= limit:
                break

    return results


def get_company_by_code(corp_code: str) -> dict | None:
    """고유번호로 기업 조회"""
    for code, name, stock, sector in COMPANIES:
        if code == corp_code:
            return {
                "corp_code": code,
                "corp_name": name,
                "stock_code": stock,
                "sector": sector,
            }
    return None


def get_all_companies() -> list[dict]:
    """전체 기업 목록"""
    return [
        {
            "corp_code": corp_code,
            "corp_name": corp_name,
            "stock_code": stock_code,
            "sector": sector,
        }
        for corp_code, corp_name, stock_code, sector in COMPANIES
    ]
